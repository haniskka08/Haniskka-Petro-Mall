// ─── Pincode Validation via India Post API ────────────────────────────────────

export interface PincodeResult {
  valid: boolean
  message: string
  district?: string
  state?: string
  postOffices?: string[]
}

export async function validatePincode(pincode: string): Promise<PincodeResult> {
  if (!/^\d{6}$/.test(pincode)) {
    return { valid: false, message: 'Pincode must be exactly 6 digits.' }
  }
  try {
    const res = await fetch(`https://api.postalpincode.in/pincode/${pincode}`)
    if (!res.ok) throw new Error('Network error')
    const data = await res.json()
    const block = data[0]
    if (!block || block.Status === 'Error' || !block.PostOffice?.length) {
      return { valid: false, message: `Pincode ${pincode} does not exist.` }
    }
    const po = block.PostOffice[0]
    const postOffices = block.PostOffice.map((p: any) => p.Name)
    return {
      valid: true,
      message: `✓ ${po.District}, ${po.State}`,
      district: po.District,
      state: po.State,
      postOffices,
    }
  } catch {
    // If API unreachable, fall back to basic format check only
    return { valid: true, message: '(Could not verify — format looks correct)' }
  }
}
